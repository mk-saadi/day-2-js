// how to add an element to an existing array, use(.push)
var numbers=[654, 231, 45, 8, 265, 3546];
numbers.push(2001);
console.log(numbers);

var friends=['baal', 'abal', 'kabaal', 'faabaal'];
friends.push('mizuhara');
friends.push('chizuru');
console.log(friends);

// how to pop/remove last element from an existing array(.pop()) using pop will only remove very last element
friends.pop();
friends.pop();
console.log(friends);
